function hoverColor(obj) {
    if(obj.style.color=='orange'){
        obj.style.color='#fff';
    }
    else{
        obj.style.color='orange';
    }
}
function hoverMenu() {
    var menu= document.getElementById("menu2");
    if(menu.style.display=='block'){
        menu.style.display='none';
    }
    else{
        menu.style.display='block'
    }
}
